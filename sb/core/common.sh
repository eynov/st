#!/bin/bash
# ==============================================================================
# 核心公共底座：路径常量 + 工具函数
# ==============================================================================

BASE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

# ------------------------------------------------------------------------------
# 路径常量
# ------------------------------------------------------------------------------
CERT_DIR="${BASE_DIR}/certs"
OUTPUT_DIR="${BASE_DIR}/output"
OUTPUT_CONFIG="${OUTPUT_DIR}/config.json"
OUTPUT_SUB="${OUTPUT_DIR}/sub.yaml"
STATE_FILE="${BASE_DIR}/instances.json"
SB_BIN="/usr/local/bin/sing-box"
SB_SERVICE="sb-core"

# ------------------------------------------------------------------------------
# 输出格式化
# ------------------------------------------------------------------------------
ok()   { echo -e "✅ $*"; }
err()  { echo -e "❌ $*" >&2; }
warn() { echo -e "⚠️  $*"; }
info() { echo -e "ℹ️  $*"; }

# ------------------------------------------------------------------------------
# 端口合法性校验
# 用法: port_valid <port>  → 返回 0/1
# ------------------------------------------------------------------------------
port_valid() {
    local port="$1"
    [[ "$port" =~ ^[0-9]+$ ]] && (( port >= 1 && port <= 65535 ))
}

# ------------------------------------------------------------------------------
# 端口占用检测（区分 TCP / UDP）
# 用法: port_used_tcp <port>
#       port_used_udp <port>
# ------------------------------------------------------------------------------
port_used_tcp() {
    ss -tlnp | awk '{print $4}' | grep -qE "(^|:)${1}$"
}

port_used_udp() {
    ss -ulnp | awk '{print $4}' | grep -qE "(^|:)${1}$"
}

# ------------------------------------------------------------------------------
# 公网 IP 获取
# ------------------------------------------------------------------------------
get_ipv4() {
    local ip
    ip=$(curl -4 -s --max-time 5 ifconfig.me 2>/dev/null \
        | grep -Eo '([0-9]{1,3}\.){3}[0-9]{1,3}' | head -1)
    [ -z "$ip" ] && ip=$(curl -4 -s --max-time 5 api.ipify.org 2>/dev/null \
        | grep -Eo '([0-9]{1,3}\.){3}[0-9]{1,3}' | head -1)
    [ -z "$ip" ] && ip=$(ip -4 route get 1.1.1.1 2>/dev/null \
        | awk '{print $7}' | head -1)
    echo "$ip"
}

get_ipv6() {
    local ip
    ip=$(curl -6 -s --max-time 5 ifconfig.me 2>/dev/null \
        | grep -Eo '([0-9a-fA-F:]+:+[0-9a-fA-F]+)' | head -1)
    [ -z "$ip" ] && ip=$(curl -6 -s --max-time 5 api6.ipify.org 2>/dev/null \
        | grep -Eo '([0-9a-fA-F:]+:+[0-9a-fA-F]+)' | head -1)
    [ -z "$ip" ] && ip=$(ip -6 route get 2001:4860:4860::8888 2>/dev/null \
        | awk '{print $7}' | head -1)
    echo "$ip"
}

# ------------------------------------------------------------------------------
# URL Encode
# ------------------------------------------------------------------------------
urlencode() {
    local raw="$1"
    local length="${#raw}"
    local i c
    for (( i = 0; i < length; i++ )); do
        c="${raw:i:1}"
        case "$c" in
            [a-zA-Z0-9.~_-]) printf '%s' "$c" ;;
            *) printf '%%%02X' "'$c" ;;
        esac
    done
}

# ------------------------------------------------------------------------------
# 终端二维码渲染
# ------------------------------------------------------------------------------
show_qr() {
    local data="$1"
    if command -v qrencode >/dev/null 2>&1; then
        echo -e "\n📷 扫描二维码快捷导入节点:"
        qrencode -t ansiutf8 "$data"
    else
        warn "未检测到 qrencode，执行 'apt install qrencode' 可开启二维码支持。"
    fi
}

# ------------------------------------------------------------------------------
# 初始化目录结构
# ------------------------------------------------------------------------------
ensure_dirs() {
    mkdir -p "$CERT_DIR" "$OUTPUT_DIR"
    [ -f "$STATE_FILE" ] || echo '{"instances":{}}' > "$STATE_FILE"
}
